<h1>Das Zeitalter der Zwietracht</h1>

<?php cap("E"); ?>

<p>ntgegen Brágons Hoffnung gab Moshran nie das Gelöbnis auf, die ihm gegenüber gezeigte
Feindseligkeit zurück zu zahlen. In aller Verschwiegenheit erschuf Moshran drei unsterbliche
Wesen. Er gab diesen Geschöpfen ungeheure Kräfte und lehrte ihnen vieles, aber er band sie auch
in vielerlei Weise an sich selbst, um sich in aller Ewigkeit ihrer Loyalität zu versichern. Er
nannte sie <a href="de_moshran_helper.php">Dragorog, Bjolmur und Cherass</a> und hauchte ihnen beinahe
gottgleiche Kräfte ein.</p>

<p>Getrieben von dem unerträglichen Verlangen nach mehr Macht, suchte Moshran nach den
Überresten der einst so mächtigen Armee von Madrook Na'han. Er fand sie, versteckt in den
tiefsten Höhlen und wurde von ihnen als ihr Führer anerkannt, da sie die Macht und die Stimme
ihres früheren Herrn erkannten.</p>

<p>Dämonengleiche Geschöpfe, augenlose Biester und unaussprechliche Schrecken holte er hervor.
Er züchtete sie und ließ sie auf die Sterblichen los, sogar auf die Orks, seine eigenen
Geschöpfe.</p>

<p>Gegen den Widerstand der anderen Götter begann er, seinen Einflussbereich zu vergrößern. Zum
ersten Mal agierten seine drei Schöpfungen völlig offen und eroberten Land und Leute, ohne die
Gebiete der anderen Götter zu respektieren. Moshran war sogar in der Lage, den Einfluss Tanoras
über einige Gewässer in seinen Gebieten zu brechen, bis ihr dies Gewahr wurde und sie die
anderen Orte schützte. Zhambra, Irmorom und Malachín versuchten mit Moshran zu sprechen, er aber
lachte nur über sie und rief seine drei unheiligen Geschöpfe und eine Horde von Ungetümen und
verdorbenen Tieren, um sie zu bekämpfen. Nach einem langen Kampf gegen vier starke Gegner
mussten sich Zhambra, Irmorom und Malachín zurückziehen.</p>

<p>Neben den Orks schworen auch Angehörige anderer Rassen, in der Hauptsache Menschen, Moshran
die Treue, da seit Urzeiten Macht und Erfolg ihre Anhänger hatte. Moshran schaffte es,
Zwietracht zwischen den verschiedenen Rassen zu sähen. Und sein Ansinnen fiel auf fruchtbaren
Boden, denn Vorurteile und Hass gegenüber anderen Rassen entstand und begann, wie eine
unaufhaltsame Seuche zu grassieren. Misstrauen beherrschte die Gedanken vieler.</p>

<p>Während die anderen Götter beratschlagten, was zu tun sei, begannen Moshrans Horden,
Festungen und Außenposten unter der Oberfläche zu errichten, von wo aus sie die Länder mit
Schrecken bedeckten. Die anderen Götter entschieden sich, einigen auserwählten Sterblichen,
Stärke und Macht zu verleihen, die über das normale Maß der Dinge hinaus ging, um so im Kampf
gegen Untoten und anderen Ungetümen zu bestehen. Die anderen Rassen begannen nun, sich selbst
mit einer verblüffenden Effizienz gegen die Kreaturen Moshrans zu verteidigen. In den langen
Kriegen, die seit da an herrschten, wurden viele neue starke Runen von den sterblichen Magiern
erforscht und geschaffen.</p>

<p>Zwischenzeitlich bemerkte Brágon seinen Fehler und kehrte zurück aus der Welt, in die er sich
zurückgezogen hatte. Mit sich brachte er einige seiner Geschöpfe, edle und mächtige Drachen. Als
er die Situation erkannte und die vom Krieg verheerten Länder sah, bot er den anderen Göttern
seine Unterstützung an, um die Ordnung wiederherzustellen. In gewaltigen Schlachten, die nun
folgten, und mit der Hilfe von Brágons Drachen wurden Moshrans Horden zurückgeschlagen. Viele
verlorene Gebiete wurden zurückgewonnen, doch der Blutzoll, mit dem diese Erfolge erzielt
wurden, war auf beiden Seiten hoch. In der entscheidenden großen Schlacht mussten die drei
Helfer Moshrans fliehen, während Moshran von Malachín geschlagen wurde. Die umliegenden Länder
wurden erschüttert und zerstört durch die tobenden Kräfte in diesem Kampf, für Jahrhunderte
unbewohnbar gemacht, so als ob jedes noch so kleinste Stück leben aus ihnen entzogen wurde. Und
doch gelang es währenddessen einem Anhänger Moshrans mit einer Schar Dämonen einige Eier aus
Brágons Drachenzucht zu stehlen.</p>

<p>Die Kontinente wurden in den langen Kriegen und der letzten Schlacht so verwüstet, dass nur
noch die sie umgebenden Inseln übrig blieben, um von den Völkern bewohnt zu werden. So groß war
die Verwüstung und so verheerend die Auswirkungen, dass alle Götter, Moshran eingeschlossen,
einen Eid schworen, niemals wieder die Hand zum Kampfe gegen ihresgleichen zu erheben.</p>

<p>Sogar nach seiner Niederlage war Moshran der Überzeugung, dass er berechtigt war, über diese
Welt und all ihre Lebewesen zu herrschen. Und so nahmen ihn die anderen Götter gefangen und
sperrten ihn in eine Zelle tief unter der Oberfläche in der Hoffnung, dass er eines Tages zu
sich selbst zurückfinden würde. Doch bevor sie ihn dort zurückließen, nahmen sie ihm noch die
Rüstung und das Schwert Madrook Na'hans. Jedem war bewusst, dass Moshran ohne diese
Artefakte die bösen Mächte, die er einst beherrschte, nicht zurückerlangen konnte, und er so
wahrscheinlich eines Tages von dem bösen Einfluss befreit werden würde. Tanora erhielt die
verfluchte Rüstung und verbarg sie an einem geheimen Ort in ihrem Unterwasserreich und
versprach, dass keiner je wieder Hand an sie legen würde. Genauso tat es Elara mit dem
verfluchten Schwert.</p>

<p>Nachdem die Kriege nun vorbei waren und die drei Anhänger Moshrans sich an unbekannte Orte
zurückgezogen hatten, gab es für Brágon keinen Grund mehr auf Illarion zu verweilen. Doch bevor
er diese Welt wieder verließ, bemerkte er den Diebstahl an seiner Drachenbrut und so gab er
Tanora und auch einigen der Jüngeren Götter ebenfalls Dracheneier, um das Gleichgewicht der
Kräfte wieder herzustellen.</p>

<p>Und so endet das Zeitalter der Zwietracht und beginnt das Zeitalter der Isolation.</p>

