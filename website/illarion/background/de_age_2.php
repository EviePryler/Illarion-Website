<h1>Das Zeitalter der Dämmerung</h1>

<p>Letzten Endes, um vor einem mächtigem Feind zu flüchten, kamen die alten Götter in diese Welt
und gaben Ihr den Namen "Illarion".</p>

<?php cap("I"); ?>

<p>n dem Bedürfnis sich vor dieser anderen weit entfernten Macht zu schützen, begannen Sie die
Welt nach ihren Vorstellungen zu formen. Sie vereinten ihre Kräfte und schufen allein mit ihrem
Willen die formlosen Elemente. Sie woben sie zusammen, schufen dadurch Struktur und Materie und
belebten die Welt mit einer Energie, die von uns in der heutigen Zeit "Magie" genannt
wird. Einmal erschaffen, begann die Welt diese Energie selbst zu erzeugen und an viele Orten und
in großen Mengen anzureichern und auszustrahlen.</p>

<p>Dann erschufen die Alten die ersten vier sterblichen Rassen: Menschen, Elfen, Zwerge und
Halblinge.</p>

<p>Jahrhunderte später erwählten die Alten elf herausragende Vertreter aus der wachsenden Zahl
der Sterblichen, um sie auszubilden und zu unterrichten, mit dem Ziel ihnen die Führung der
Völker in einem kommenden Kampf zu übertragen.</p>

<p>Ihre enge Verbindung zu den Göttern änderte ihre sterbliche Seele in mehr als nur einer
Weise. Die alten Götter änderten deren Existenz und machten sie ihnen gleich. Wodurch sie die
Schranken des Lebens und des Todes durchbrachen und später als die jüngeren Götter bekannt
wurden.</p>

<p>Unter diesen jüngeren Göttern gab es einen, der sich selbst Moshran nannte. Er war der
bekannteste und am meisten respektierte unter den jüngeren Göttern. Er stand den einfachen
Sterblichen am nächsten, ein wahrer Freund der Massen: Er hatte unerschütterliche Prinzipien und
war stolz auf seine Position, doch diente er den Völkern in vielerlei Hinsicht und vergass nie
seinen Ursprung und seine Aufgabe in dieser Welt. Viele schauten zu ihm in tiefstem Respekt auf
und suchten bei ihm nach Führung und Hilfe.</p>

<p>Die erste große Stadt, Nabranoo, wurde gegründet und zur Residenz der Götter Illarions
erkoren. Andere Städte folgten, denn die unterschiedlichen Völker vermehrten sich rasch.</p>

<p>Hunderte von Jahren vergingen ohne dass ein Zeichen der Gefahr den Horizont verdüsterte,
wodurch den Göttern genügend Zeit geblieben war, sich hinreichend vorzubereiten. Aber wie
erwartet und befürchtet fand eines Tages der namenlose Feind einen Weg in diese Welt.</p>

<p>Und so endet das Zeitalter der Dämmerung und beginnt das Zeitalter des Kampfes.</p>

