<h1>Das Zeitalter der Eintracht</h1>

<?php cap("N"); ?>

<p>ach dem gewaltigen Krieg, der beinahe das Ende von dieser Welt bedeutet hätte, begannen die
Völker einen Wiederaufbau auf den Ruinen der alten Zivilisation. Nach und nach verteilten sie
sich über alle Kontinente Illarions.</p>

<p>Die alten Götter, die durch den Kampf und durch die erfolgreiche Versiegelung der Portale
aufs äußerste erschöpft waren, zogen sich aus Illarion zurück. Nur Tanora, die sich in einen
Menschen mit dem Namen Manon Petranor verliebte, verweilte weiterhin auf Illarion. Ihre
Nachkommen entwickelten sich zu den Echsenmenschen, fähig sowohl Luft wie auch Wasser zu
atmen.</p>

<p>Ein Großteil der Sterblichen wurde seßhaft. Langsam verblasste die Erinnerung an den
verheerenden Krieg und wurde verdrängt von alltäglichen Problemen. Nur noch die
Geschichtsschreiber bewahrten die alte Aufzeichnungen, die von den schrecklichen Ereignissen des
Krieges kündeten.</p>

<p>Da es den ursprüngliche Grund für die Existenz der Völker, die Verteidigung von Illarion,
nicht mehr gab, wurde nach neuen Zielen, nach einer neuen Aufgabe gesucht. Sie sehnten sich nach
Führung und einen Sinn für Ihr Leben, wo sonst nur Leere und Einsamkeit herrschen würde. Und so
wählten die meisten einen der Götter aus, dessen Persönlichkeit und Lebensart Ihrer eigenen am
nächsten kam. Diesen verehrten sie und dienten Ihm so gut sie konnten.</p>

<?php cap("A"); ?>

<p>ber obwohl Moshran am meisten zu ihrer Freiheit beigetragen hat, erwählten nur wenige ihn zu
ihrem Gott. Seine Laune wurde im Laufe der Jahre immer düsterer und undurchdringlicher. Es
schien, als ob der einst bewunderte Charakter von einer anderen Persönlichkeit übernommen wurde.
Er wurde alleine gelassen und begann, die Undankbarkeit aller Rassen zu verfluchen. Er dachte:
"Sie sollten alle mir folgen, da ich es war, der ihren größten Feind getötet hat."
Seine Gedanken waren von Hass beherrscht und er gelobte, das sie eines Tages für ihr Verhalten
bezahlen würden. Der Ältere Gott Brágon sah dies und war berührt von diesem dunklen Schicksal.
Brágon wusste, dass etwas Böses schon teilweise Kontrolle über Moshran gewonnen hatte, glaubte
aber dass es noch nicht zu spät war, ihm zu helfen. Sie kamen zusammen und zusammen erschufen
sie die Orks, die Moshran dienen und dadurch in seiner Einsamkeit trösten sollten.</p>

<p>Erschaffen aus dem Feuer und angefüllt vom Zorn ihres Gottes, verbreiteten die Orks schnell
Chaos unter den Sterblichen, wo immer sie auch erschienen. Ihre unberechenbare Natur und ihre
Begierde nach Krieg und Zerstörung ließ sie in Konflikt mit den anderen Rassen geraten. Dies
wiederum erregte den Ärger der anderen Jungen Götter. Aber da aus der Rasse der Orks auch viele
ehrenvolle Krieger kamen, wurde diese Schöpfung nicht abgeändert oder ungeschehen gemacht.</p>

<p>So endet das Zeitalter der Eintracht und beginnt das Zeitalter der Zwietracht.</p>

